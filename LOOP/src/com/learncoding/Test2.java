package com.learncoding;

public class Test2 {
	//if else-if ladder
	public static void main(String[] args) {
		
		int i=110;
		int j=20;
		
		if (i+j==20) {
			System.out.println("Result is incorrect");
		}else if(i+j==30) {
			System.out.println("Result is correct");
		}

		
		else {
			System.out.println("All Result are incorrect");
		}
	}

}
